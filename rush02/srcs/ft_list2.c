#include "rush02.h"

t_cell	*free_list(t_cell *l)
{
	t_cell *tmp;

	tmp = NULL;
	while (l)
	{
		tmp = l->next;
		free(l->key);
		free(l->value);
		free(l);
		l = tmp;
	}
	return (l);
}
